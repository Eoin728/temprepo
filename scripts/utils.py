# Plot training history
# Training history: dict containing
# {'train_loss', 'val_loss', 'train_acc', 'val_acc'}
def plot_training_history(history: dict, fontsize: int = 14):
    """
    Plot training and validation loss/accuracy on one graph.

    Left axis  : Loss
    Right axis : Accuracy

    Style:
        Train = teal
        Validation = orange
        Loss marker = circle (o)
        Accuracy marker = cross (s)

    Args:
        history (dict): Training history containing
            'train_loss', 'val_loss', 'train_acc', 'val_acc'.
        fontsize (int): Font size used for ALL plot text.
    """

    epochs = range(1, len(history["train_loss"]) + 1)

    fig, ax_loss = plt.subplots(figsize=(8, 5))

    # ---- Loss (left axis) ----
    ax_loss.plot(
        epochs, history["train_loss"],
        color="teal", marker="o", linewidth=3, label="Train Loss"
    )

    ax_loss.plot(
        epochs, history["val_loss"],
        color="orange", marker="o", linewidth=3, label="Val Loss"
    )

    # Format loss with 1 decimal place
    ax_loss.yaxis.set_major_formatter(FormatStrFormatter('%.1f'))
    ax_loss.set_xlabel("Epoch", fontsize=fontsize)
    ax_loss.set_ylabel("Loss", fontsize=fontsize)
    ax_loss.tick_params(axis="both", labelsize=fontsize)
    ax_loss.grid(alpha=0.3)

    # ---- Accuracy (right axis) ----
    ax_acc = ax_loss.twinx()

    ax_acc.plot(
        epochs, [a * 100 for a in history["train_acc"]],
        color="teal", marker="s", linestyle=":",
        linewidth=3, label="Train Acc"
    )


    ax_acc.plot(
        epochs, [a * 100 for a in history["val_acc"]],
        color="orange", marker="s", linestyle=":",
        linewidth=3, label="Val Acc"
    )

    # Format accuracy as integer
    ax_acc.yaxis.set_major_formatter(FormatStrFormatter('%d'))
    ax_acc.set_ylabel("Accuracy", fontsize=fontsize)
    ax_acc.tick_params(axis="y", labelsize=fontsize)
    ax_acc.tick_params(axis='both', direction='in', length=6, width=1.5)

    # ---- Combined legend ----
    lines = ax_loss.get_lines() + ax_acc.get_lines()
    labels = [line.get_label() for line in lines]
    
    ax_loss.tick_params(axis='both', direction='in', length=6, width=1.5)
    ax_loss.legend(lines, labels, fontsize=fontsize)
    ax_loss.xaxis.set_major_locator(MaxNLocator(integer=True))  # integer ticks, auto spacing


    plt.title("Training History", fontsize=fontsize)
    plt.tight_layout()
    plt.show()



