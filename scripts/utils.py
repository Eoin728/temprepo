# Custom Dataset for loading stroke data and labels from .h5 files.
# Supports split selection, optional preprocessing transforms, and
# lazy file loading for memory efficiency.
class H5Dataset(Dataset):
    """
    Dataset for .h5 stroke data with optional transforms.

    Args:
        file_path (str): Path to dataset file.
        vocab_obj: Vocabulary mapping tokens to indices.
        split (str): Dataset split ('train', 'valid', 'test').
        x_transform_fn (callable, optional): Transform applied to input x.
        y_transform_fn (callable, optional): Transform applied to target y.
        preprocess_args (dict, optional): Shared kwargs for transforms.
    """

    def __init__(self, file_path, vocab_obj, split='train', x_transform_fn=None, y_transform_fn=None, preprocess_args=None):

        self.file_path = file_path
        self.split = split.lower()
        self.file = None  # lazy open
        self.vocab_obj = vocab_obj
        self.x_transform = x_transform_fn
        self.y_transform = y_transform_fn
        self.preprocess_args = preprocess_args or {}

        self.x_key = f'X_{self.split}'
        self.y_key = f'Y_{self.split}'

        with h5py.File(file_path, 'r') as f:
            self.length = len(f[self.x_key])

    def __len__(self):
        return self.length

    def preprocess_x(self, x):
        x = torch.tensor(x, dtype=torch.float32)
        if self.x_transform:
            x = self.x_transform(x, **self.preprocess_args)
        return x

    def preprocess_y(self, y):
        tokens = list(y.decode('utf-8'))
        if self.y_transform:
            tokens = self.y_transform(tokens, **self.preprocess_args)
        indices = [self.vocab_obj[token] for token in tokens]
        return torch.tensor(indices, dtype=torch.long)

    def __getitem__(self, idx):
        if self.file is None:
            self.file = h5py.File(self.file_path, 'r')

        x = self.preprocess_x(self.file[self.x_key][idx])
        y = self.preprocess_y(self.file[self.y_key][idx])
        return x, y
    

