# Decode token IDs into readable text
# token_ids: sequence of token indices
# vocab_obj: vocabulary with lookup_token()
# pad_value: padding token id to ignore
def decode_tokens(token_ids, vocab_obj, pad_value=None):
    """
    Convert a sequence of token IDs into a readable string.

    Pads are removed and special symbols are replaced for display.

    Args:
        token_ids (Iterable[int]): Sequence of token indices.
        vocab_obj: Vocabulary object with lookup_token().
        pad_value (int): Padding token id to ignore.

    Returns:
        str: Decoded token string.
    """
    return " ".join(
        vocab_obj.lookup_token(int(i))
        .replace("*", "×")
        .replace("/", "÷")
        .replace(",", "<eon>")
        for i in token_ids
        if int(i) != pad_value
    )


