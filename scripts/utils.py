# Compute LA
def batch_LA(y_batch, y_hat_batch, pad_id, bos_id=None, eos_id=None):
    """
    y_batch: ground truth [B, T]
    y_hat_batch: predicted [B, T]
    Returns: scalar LA
    """
    y_batch_trim = preprocess_sequences(y_batch, pad_id, bos_id, eos_id)
    y_hat_trim  = preprocess_sequences(y_hat_batch, pad_id, bos_id, eos_id)

    total_acc = 0.0

    for gt_seq, pred_seq in zip(y_batch_trim, y_hat_trim):
        # compute edit distance
        d = editdistance.eval(gt_seq, pred_seq)
        # normalize by sequence length
        seq_acc = 1 - d / max(len(gt_seq), 1)
        total_acc += seq_acc

    return total_acc / len(y_batch_trim)

