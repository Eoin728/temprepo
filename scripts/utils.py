# Utility class to convert stroke tensors into SVG path strings
# - Converts each tensor row into a vector path
# - Removes special tokens (PAD, BOS, EOS) before visualization
# - Generates SVG path commands for rendering strokes
class SVGPath(object):
    """
       Generates an SVG path string from a tensor of stroke vectors.
    """

    def __init__(self, input_tensor=None):
        # Store tensor as a list of vectors; empty if no input
        self.path = input_tensor.tolist() if input_tensor is not None else []

    def isEmpty(self):
        # Check if path is empty
        return not self.path

    def drawPath(self, offset, max_height, max_width):
        # Return SVG path string for stored stroke vectors
        if self.isEmpty():
            return ""
        svg_path_str = ""
        x = [elem for index, elem in enumerate(self.path) if not index % 2]
        y = [elem for index, elem in enumerate(self.path) if index % 2]
        assert len(x) == len(y), "Invalid x and y correspondence in input path"
        for i, (xi, yi) in enumerate(zip(x, y)):
            svg_path_str += "M" if i == 0 else "L"
            svg_path_str += f"{offset + xi * max_width},{yi * max_height} "
        return svg_path_str
    

