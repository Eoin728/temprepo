# Get DataLoader
# Helper function, using cache
def get_dataloader(
        h5_file: Path, 
        batch_size: int, 
        split: str,
        loader_setup: Tuple[
            Vocab,     # vocab
            dict,      # prepocessing dictionary
            Callable,  # preprocess_x_fn
            Callable,  # preprocess_y_fn
            Callable   # pad_collate_fn
        ],
        shuffle: bool =False,
        use_cache: bool = False,
        prefix: str = ""
    ):
    """
    Preprocesses H5Dataset, applies pad_collate, caches batches to disk, and shows a progress bar.
    Returns a list of pre-padded batches.
    """
    (vocab_obj, preprocess_args, x_transform_fn, y_transform_fn, pad_collate_fn) = loader_setup

    cache_dir = Path(h5_file.parent, 'cache', prefix)
    cache_dir.mkdir(parents=True, exist_ok=True)
    
    # Use H5 file stem to prepend cache file name
    h5_prefix = h5_file.stem  # e.g., 'postfix_208k'
    cache_file = cache_dir / f"{h5_prefix}_{split}_bs{batch_size}.pt"
    
    if use_cache and cache_file.exists():
        print(f"Loading cached {split} batches from: {cache_file.parent.parent.name}/{cache_file.parent.name}/{cache_file.name}")
        cached_batches = torch.load(cache_file)
        loader = CachedLoader(cached_batches, shuffle=shuffle)
    else:
        print(f"Processing {split} batches from: {h5_file.parent.name}/{h5_file.name}")
        dataset = H5Dataset(
            h5_file,
            vocab_obj,
            split=split,
            x_transform_fn=x_transform_fn,
            y_transform_fn=y_transform_fn,
            preprocess_args=preprocess_args,
        )
        loader = DataLoader(
            dataset, 
            batch_size=batch_size, 
            shuffle=True,  
            collate_fn=lambda batch: pad_collate_fn(batch, **preprocess_args)
        )
        if use_cache:
            cached_batches = []
            for batch in tqdm(loader, desc=f"Caching {split} batches"):
                cached_batches.append(batch)
            
            torch.save(cached_batches, cache_file)
            print(f"Saved cached {split} batches to: {cache_file.parent.parent.name}/{cache_file.parent.name}/{cache_file.name}")
            loader = CachedLoader(cached_batches, shuffle=shuffle)
    
    return loader


