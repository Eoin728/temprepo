# Wrap cached batches in a DataLoader-like class
class CachedLoader:
    def __init__(self, batches, shuffle=False):
        self.batches = batches
        self.shuffle = shuffle

    def __iter__(self):
        if self.shuffle:
            import random
            shuffled = self.batches.copy()
            random.shuffle(shuffled)
            for batch in shuffled:
                yield batch
        else:
            for batch in self.batches:
                yield batch

    def __len__(self):
        return len(self.batches)


