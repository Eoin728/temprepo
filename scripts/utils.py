# Trim PAD and cut at EOS for a batch of sequences (tensor)
def preprocess_sequences(batch, pad_id, bos_id=None, eos_id=None):
    """Trim PAD and cut at EOS for a batch of sequences (tensor)."""
    batch_list = batch.cpu().tolist()
    trimmed = []
    for seq in batch_list:
        # Remove BOS if it is at the start
        if bos_id is not None and seq[0] == bos_id:
            seq = seq[1:]
        # Remove PAD
        seq = [t for t in seq if t != pad_id]
        # Cut at EOS
        if eos_id is not None and eos_id in seq:
            seq = seq[: seq.index(eos_id) + 1]
        trimmed.append(seq)
    return trimmed


