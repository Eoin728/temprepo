# Convert a batch of stroke sequences into an SVG string for visualization
# - Removes special tokens (PAD, BOS, EOS, zero padding) before rendering
# - Adds visual dividers between strokes
# - Generates scalable SVG string with configurable height and width
def strokes_to_svg(
    x: torch.Tensor,
    size: dict,
    bos_value: int = 2,
    eos_value: int = 3,
    pad_value: int = -5,
    zero_pad_value: int = 0,
    vector_size: int = 128,
    HEIGHT: int = 50,
) -> str:
    """
    Convert a 2D stroke tensor into an SVG formatted string.

    Args:
        x (torch.Tensor): Stroke tensor of shape (num_strokes, vector_size)
        size (dict): Dictionary with keys 'width' and 'height' for SVG scaling
        bos_value (int): Beginning-of-sequence token
        eos_value (int): End-of-sequence token
        pad_value (int): Padding token
        zero_pad_value (int): Optional zero-padding token
        vector_size (int): Expected vector dimension of each stroke
        HEIGHT (int): Output SVG height

    Returns:
        str: SVG string representing the strokes
    """

    assert x.shape[-1] == vector_size and x.ndim == 2, \
        f"Expected shape (*, {vector_size}), got {x.shape}"

    mask = ~(
        (x == bos_value).all(dim=1) |
        (x == eos_value).all(dim=1) |
        (x == pad_value).all(dim=1) |
        (x == zero_pad_value).all(dim=1)
    )
    x = x[mask]

    paths = []
    for p in x:
        p = p[p != pad_value]
        p = p[p != zero_pad_value]
        new_path = SVGPath(p)
        if not new_path.isEmpty():
            paths.append(new_path)

    paths.append(SVGPath()) # bos
    paths.insert(0, SVGPath()) # eos

    dividers = ""
    info = ""
    svgPath = "<path d=\" "

    def fun(j, pa):
        return pa.drawPath(offset=size['width'] * j, max_height=size['height'], max_width=size['width'])

    for i, p in enumerate(paths[:-1]):
        dividers = dividers + f"<line x1=\"{size['width'] * (i + 1)}\" y1=\"0\" x2=\"{size['width'] * (i + 1)}\" y2=\"{size['height']}\"/>" + "\n\t"
        svgPath = svgPath + str("" if p.isEmpty() else fun(i, p))

    svgPath += "\" />"
                
    TEMPLATE = f"<?xml version=\"1.0\" standalone=\"no\"?>\n\
        <svg height=\"{HEIGHT}\" width=\"{(size['width'] / size['height']) * HEIGHT * len(paths)}\"\
        viewBox=\"0 0 {size['width'] * len(paths)} {size['height']}\" style=\"background-color:white\" xmlns=\"http://www.w3.org/2000/svg\" version=\"1.1\">\n\
        <title>Strokes</title>\n\
        <desc>Stroke Sequence</desc>\n\
        <g fill=\"#E0E0E0\" stroke=\"none\" stroke-width=\"{size['width'] / 30}\" stroke-linecap=\"round\" stroke-linejoin=\"round\">\n\
            <rect x=\"0\" y=\"0\" width=\"{size['width'] * len(paths)}\" height=\"{size['height']}\"/>\"\n\
        </g>\n\
        <g fill=\"#00D000\" stroke=\"none\" fill-opacity=\"0.58\">\n\
            <rect x=\"0\" y=\"0\" width=\"{size['width']}\" height=\"{size['height']}\"/>\"\n\
        </g>\n\
        <g fill=\"#D00000\" stroke=\"none\" fill-opacity=\"0.58\">\n\
            <rect x=\"{size['width'] * (len(paths) - 1)}\" y=\"0\" width=\"{size['width']}\" height=\"{size['height']}\"/>\"\n\
        </g>\n\
        <g fill=\"none\" stroke=\"#000000\" stroke-width=\"{size['width'] / 40}\" stroke-linecap=\"round\" stroke-linejoin=\"round\">\n\
            <rect x=\"0\" y=\"0\" width=\"{size['width'] * len(paths)}\" height=\"{size['height']}\"/>\"\n\
        </g>\n\
        <g fill=\"none\" stroke=\"#303030\" stroke-dasharray=\"{size['width'] / 18},{size['width'] / 18}\" stroke-width=\"{size['width'] / 50}\" stroke-linecap=\"round\" stroke-linejoin=\"round\">\n\
            {dividers}\n\
        </g>\n\
        <g fill=\"red\" font-size=\"{size['width'] / 60}em\" font-family=\"Arial\">\n\
            {info}\n\
        </g>\n\
        <g fill=\"none\" stroke=\"black\" stroke-width=\"{size['width'] / 30}\" stroke-linecap=\"round\" stroke-linejoin=\"round\">\n\
            {svgPath}\n\
        </g>\n\
    </svg>\n\
    "
    return TEMPLATE


